/** Returns the icon + colour config for an audit event type */
export function auditTypeConfig(type) {
  const map = {
    RECEIVED:         { icon: IconInbox,       color: "#60a5fa", label: "Received" },
    TRANSLATED:       { icon: IconArrowRight,  color: "#818cf8", label: "Translated" },
    DISPATCHED:       { icon: IconZap,         color: "#34d399", label: "Dispatched" },
    CONFIRMED:        { icon: IconCheckCircle, color: "#34d399", label: "Confirmed" },
    FAILED:           { icon: IconXCircle,     color: "#f87171", label: "Failed" },
    CONFLICT_DETECTED:{ icon: IconAlertTriangle, color: "#a78bfa", label: "Conflict Detected" },
    CONFLICT_RESOLVED:{ icon: IconShield,      color: "#34d399", label: "Conflict Resolved" },
    RETRY_QUEUED:     { icon: IconRefresh,     color: "#fbbf24", label: "Retry Queued" },
    DLQ_PARKED:       { icon: IconInbox,       color: "#f87171", label: "Dead-Lettered" },
    SCHEMA_DRIFT_DETECTED: { icon: IconAlertTriangle, color: "#fbbf24", label: "Schema Drift" },
  };
  return map[type] || { icon: IconClock, color: "#9ca3af", label: type || "Unknown" };
}
